package com.ksit.nuclearwinter.radiation;

// Константы системы расчёта радиации
// В будущем можно читать из config файла
public final class RadiationConfig {

    private RadiationConfig() {} // утилитный класс - не инстанциировать

    // ЗАТУХАНИЕ

    // Множитель затухания радиации чанка каждую секунду
    // radiationLevel *= (1 - DECAY_RATE) каждый тик
    public static final float DECAY_RATE = 0.000001f;

    // --- константы ---
    // ================================================================
    // СИНЕРГИЯ
    // Общий множитель силы синергии.
    // Определяет максимально возможный прирост радиации от взаимодействия
    // источников. При K=0.15 максимальный бонус - +15% к суммарной мощности.
    // Увеличение: источники усиливают друг друга сильнее, радиация растёт
    // быстрее при скоплениях. Уменьшение: синергия почти не ощущается.
    // Рекомендуемый диапазон: 0.05 – 0.30
    public static final float K_SYNERGY = 0.15f;

    // ================================================================
    // LAMBDA - характерный масштаб расстояния (в чанках)
    // Определяет как быстро резонанс затухает с расстоянием между источниками.
    // Используется в: phi = exp(-spread / LAMBDA)
    // При spread == LAMBDA резонанс равен exp(-1) ≈ 0.37 от максимума.
    // При spread == 2*LAMBDA резонанс ≈ 0.14, при 3*LAMBDA ≈ 0.05.
    // Увеличение: источники "чувствуют" друг друга на большем расстоянии,
    //             синергия размазывается по большой территории.
    // Уменьшение: синергия только у вплотную стоящих источников.
    // Рекомендуемый диапазон: 1.0 – 8.0
    public static final float LAMBDA = 3.0f;

    // ================================================================
    // BETA_G - штраф за неравномерность мощностей (через CV)
    // Используется в: phi = exp(-BETA_G * cv)
    // CV=0 (все источники равны)  → штраф = exp(0) = 1.0 (нет штрафа)
    // CV=0.5 (умеренный разброс)  → штраф = exp(-1.0) ≈ 0.37
    // CV=1.0 (сильный разброс)    → штраф = exp(-2.0) ≈ 0.14
    // CV=2.0 (один доминирует)    → штраф = exp(-4.0) ≈ 0.02
    // Увеличение: система требует всё более однородных источников
    //             для получения синергии. Одиночный мощный блок
    //             среди слабых почти не даёт бонуса.
    // Уменьшение: неравномерность почти не штрафуется.
    // Рекомендуемый диапазон: 0.5 – 4.0
    public static final float BETA_G = 2.0f;

    // ================================================================
    // ALPHA_N - степень роста синергии от количества источников
    // Используется в: psi = n^ALPHA_N / (1 + MU * n^ALPHA_N)
    // ALPHA_N = 1.0 - линейный рост с количеством
    // ALPHA_N = 0.5 - корневой рост (медленнее насыщается)
    // ALPHA_N = 0.6 - компромисс: быстрый старт, плавное насыщение
    //
    // n=1:  n^0.6 = 1.00
    // n=4:  n^0.6 = 2.30
    // n=9:  n^0.6 = 3.74
    // n=16: n^0.6 = 5.28
    // n=25: n^0.6 = 6.90
    //
    // Увеличение: каждый новый источник даёт больше синергии,
    //             скопления становятся намного опаснее одиночек.
    // Уменьшение: количество почти не влияет, важнее мощность.
    // Рекомендуемый диапазон: 0.3 – 1.0
    public static final float ALPHA_N = 0.6f;

    // ================================================================
    // MU - коэффициент насыщения по количеству источников
    // Используется в: psi = n^ALPHA_N / (1 + MU * n^ALPHA_N)
    // Ограничивает psi сверху: при n→∞, psi → 1/MU
    // MU=0.1  → максимум psi = 10.0
    // MU=0.2  → максимум psi = 5.0
    // MU=0.5  → максимум psi = 2.0
    //
    // Физический смысл: после какого-то количества источников
    // добавление нового почти ничего не меняет - система насыщена.
    // Увеличение: насыщение наступает раньше, скопления из
    //             100 блоков не страшнее чем из 20.
    // Уменьшение: насыщения почти нет, 1000 блоков = катастрофа.
    // Рекомендуемый диапазон: 0.05 – 0.5
    public static final float MU = 0.1f;

    // ================================================================
    // GAMMA_DOM - степень штрафа за доминирование одного источника
    // Используется в: omega = 1 - (max / S) ^ GAMMA_DOM
    // Если один источник даёт долю D от суммы:
    // GAMMA_DOM=1: omega = 1 - D         (линейный штраф)
    // GAMMA_DOM=2: omega = 1 - D^2       (мягкий штраф)
    // GAMMA_DOM=3: omega = 1 - D^3       (очень мягкий)
    //
    // D=0.5 (один источник = 50% суммы):
    //   GAMMA=1 → omega=0.50
    //   GAMMA=2 → omega=0.75
    //   GAMMA=3 → omega=0.88
    //
    // D=0.9 (один источник = 90% суммы):
    //   GAMMA=1 → omega=0.10
    //   GAMMA=2 → omega=0.19
    //   GAMMA=3 → omega=0.27
    //
    // Увеличение: один мощный источник почти не теряет синергию
    //             (штраф смягчается). Уменьшение: доминирующий
    //             источник полностью гасит синергию.
    // Рекомендуемый диапазон: 1.0 – 4.0
    public static final float GAMMA_DOM = 2.0f;

    // ================================================================
    // S0 - полунасыщение мягкого порога
    // Используется в: soft_thresh = S / (S + S0)
    // При S == S0: soft_thresh = 0.5 (синергия в полсилы)
    // При S == 3*S0: soft_thresh = 0.75
    // При S == 9*S0: soft_thresh = 0.90
    //
    // S0=5:  синергия в полную силу уже при S~15-20
    // S0=10: синергия в полную силу при S~30-40
    // S0=20: синергия раскрывается только при очень высокой мощности
    //
    // Физический смысл: минимальный "порог загрязнения" при котором
    // источники начинают заметно взаимодействовать.
    // Увеличение: синергия только при серьёзном загрязнении,
    //             одиночные слабые источники не взаимодействуют.
    // Уменьшение: даже слабые источники усиливают друг друга.
    // Рекомендуемый диапазон: 5.0 – 30.0
    public static final float S0 = 10.0f;

    // ================================================================
    // P_THRESH - жёсткий минимальный порог для входа в расчёт синергии
    // Если S <= P_THRESH - функция возвращает S без синергии вообще.
    // Это оптимизация: не тратить время на расчёт phi/psi/omega
    // для чанков с незначительным загрязнением.
    // P_THRESH должен быть меньше S0, иначе мягкий порог никогда
    // не сработает в полную силу до жёсткого отсечения.
    // Рекомендуемый диапазон: 2.0 – 10.0
    public static final float P_THRESH = 5.0f;
}